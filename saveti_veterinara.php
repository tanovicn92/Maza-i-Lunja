  <h3> Nega dlake i kože pasa </h3>
  <p>
Koža je najveći organ tela životinja I svakodnevno je izložena uticajima iz spoljašnje sredine. Svoju osnovnu ulogu, a to je pre svega odbrambena uloga, kao I mnoge druge fiziološke uloge (sprečava gubitak vode, štiti od fizičkih, hemijskih I mikrobnih povreda) može vršiti isključivo zdrava I kompaktna koža zajedno sa dlačnim pokrivačem. Razni paraziti, oboljenja, deficit vitamina, kao I disbalans hormona mogu dovesti do lošeg kvaliteta dlake I kože pasa. Upravo iz tog razloga veliki akcenat stavljamo na svakodnevnu negu dlake I kože ljubimaca. 
