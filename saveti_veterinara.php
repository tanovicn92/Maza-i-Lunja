<h3>Četkanje pasa </h3>
<p>
Svakodnevno četkanje Vaših ljubimaca pomoći će da se otklone nečistoće sa dlake, a dlake ce izgledati sjajno I uredno. Četkanje, posebno vlasnicima, pomaže u periodima pojačanog linjanja pasa. U zavisnosti od rase, veličine psa I kvaliteta dlake, izabraćete adekvatnu četku, česalj, rukavicu. 
</p>
<h3>Kupanje pasa </h3>
<p>
Kupanje jeste važno u nezi Vaših četvoronožnih prijatelja - pasa. Osim što morate voditi računa o temperaturi vode, izabrati adekvatan šampon, postupiti prema uputstvu za upotrebu istog, važno je da znate da kupanje ne sme biti često. Samim kupanjem možete primetiti bilo kakve promene na koži. Ako ih primetite predlažemo da ih ne rešavate sami već da se obratite veterinaru. Najčešće promene su: pojačano opadanje dlake, suva I lomljiva dlaka, svrab, crvenilo, pečati na koži. Kožna oboljenja su jako uporna I dugotrajno se leče pa je higijena iste od presudnog značaja. Stanje kože I dlake psa su prvi pokazatelj zdravlja jer ona kao najveći organ je prva na kojoj se pojave promene I ukažu da postoji drugi zdravstveni problem. Najčešći su demodikoza, seboreja, svrab, šuga... 
</p>
